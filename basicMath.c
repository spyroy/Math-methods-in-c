#include <stdio.h>
#include "myMath.h"

float add(float x, float y)
{
	float answer = x + y;
	return answer;
}

float sub(float x , float y)
{
	float answer = x - y;
	return answer;
}

double mul(double x , int y)
{
	double answer = x * y;
	return answer;
}

double div(double x , int y)
{
	double answer = x / y;
	return answer;
}

